package huayang.hychat.model.bo;

import java.util.List;

/**
 * Created by buhuayang on 16/7/28.
 */
public class Menuset {

    private String id;
    private String name;
    private String type;

    private List<Menu> menus;

    public Menuset(String id, String name, String type) {
        this.id = id;
        this.name = name;
        this.type = type;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public List<Menu> getMenus() {
        return menus;
    }

    public void setMenus(List<Menu> menus) {
        this.menus = menus;
    }
}
